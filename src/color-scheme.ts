/**
 * Enumeration representing the possible color schemes.
 * - Dark: Represents a dark color scheme preference.
 * - Light: Represents a light color scheme preference.
 * - Unsupported: Represents a scenario where the color scheme preference is not supported by the browser.
 */
export enum ColorScheme {
  Dark = "dark",
  Light = "light",
  Unsupported = "unsupported"
}
